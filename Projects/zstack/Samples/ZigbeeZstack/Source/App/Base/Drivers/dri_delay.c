#define DRI_DELAY_C_

/* ------------------------------------------------------------------------------------------------
 *                                          Includes
 * ------------------------------------------------------------------------------------------------
 */
#include "dri_delay.h"





/**************************************************************************************************
 * @fn          Delay_Ms
 * @brief       延时ms函数
 * @param       timeout -> 延时ms数,非精确延时
 * @return      无
 **************************************************************************************************
 */
void Delay_Ms( uint16 timeout )
{
   while(timeout--)
   { 
      Delay_Us(1080);     //等待1ms                          
   }
}


/**************************************************************************************************
 * @fn          Delay_Us
 * @brief       延时us函数
 * @param       timeout -> 延时us数
 * @return      无
 **************************************************************************************************
 */
void Delay_Us( uint16 timeout )
{
  while (timeout--)
  {
    asm("NOP");
    asm("NOP");
    asm("NOP");
  }
}

